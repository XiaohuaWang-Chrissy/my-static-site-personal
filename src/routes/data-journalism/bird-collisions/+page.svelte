    <p class="article-label">DATA JOURNALISM</p>
    <h1 class="article-title">Making Windows Safer for Birds</h1>
    <p class="article-byline">By <strong>Chrissy Wang</strong></p>
  </header>

  <figure class="cover-figure">
    <img src="{base}/bird-story-cover-image.webp" alt="A bird near a glass window" class="cover-image" />
  </figure>

  <div class="article-body">
    <p class="article-lede">
      Jin Bai, then a research assistant at University of North Carolina at Chapel Hill School of Education, found a dead magnolia warbler that crashed into a glass window outside the UNC-Chapel Hill's Davis Library in 2019. In 2022, Bai launched <a href="https://www.citybird.org/" target="_blank" rel="noopener">City Bird</a>, an initiative to document bird collision deaths at UNC-Chapel Hill, North Carolina State University and Meredith College.
    </p>

    <p>
      While City Bird has stopped conducting official surveys at NC State and Meredith, members of the public can still proactively submit observations on bird collisions on those campuses. At UNC-Chapel Hill, City Bird has recorded over 192 collisions since 2022 — which could be just a small fraction of the actual number, as <a href="https://peerj.com/articles/460/" target="_blank" rel="noopener">research</a> has shown that scavengers often remove bird carcasses.
    </p>

    <DataHighlight
      metric="~1 Billion"
      description="Birds in the United States die each year from crashing into windows, according to a 2014 study by Scott Loss and colleagues at the Smithsonian Migratory Bird Center."
      trend="down"
    />

    <p>
      North Carolina lies along the Atlantic Flyway, one of the most densely populated bird migration routes in North America. Urban sprawl and the rise of reflective glass buildings have turned the flyway into a hazardous trap for migrating birds, threatening waterfowl habitats in the region, according to <a href="https://www.ducks.org/conservation/where-ducks-unlimited-works/waterfowl-migration-flyways/atlantic-flyway" target="_blank" rel="noopener">Ducks Unlimited</a>, a private waterfowl and wetlands conservation organization.
    </p>

    <p>
      City Bird wasn't the first campus initiative to track bird collisions. In 2014, Natalia Ocampo-Peñuela, then a doctoral student at Duke University and now a professor at the University of California, Santa Cruz, and Duke University senior lecturer Nicolette Cagle started the Duke <a href="https://sites.duke.edu/birdcollisions/" target="_blank" rel="noopener">Bird-Window Collision Project</a> to address bird collisions. Students and faculty began systematically documenting bird deaths on campus. A <a href="https://erenweb.org/all-projects/bird-window-collisions-project/" target="_blank" rel="noopener">2013 study</a> by the Ecological Research as Education Network found that Duke had the highest rate of bird-window collisions among more than 100 campuses in the U.S.
    </p>

    <blockquote>
      "If you use hotspot mapping, you could find the exact windows where all the birds are hitting, or the buildings that are the most impactful."
      <cite>— Nicolette Cagle, Senior Lecturer, Duke University</cite>
    </blockquote>

    <DataHighlight
      metric="72%"
      description="of bird collisions on Duke's campus occurred at Fitzpatrick Center for Interdisciplinary Engineering, Medicine and Applied Sciences."
      trend="down"
    />

    <p>
      Cagle explained that birds collide with windows because transparency and reflectivity make glass look like open sky or habitat, while artificial lights further disorient them during flight.
    </p>

    <p>
      Cagle and Ocampo-Peñuela conducted a study finding that 72% of bird collisions on Duke's campus occurred at Fitzpatrick Center. Cagle said <a href="https://www.dukechronicle.com/article/2015/05/dukes-bird-safety-one-worst-nation-according-bird-window-collision-project" target="_blank" rel="noopener">coverage</a> by Duke student newspaper The Chronicle and <a href="https://pubmed.ncbi.nlm.nih.gov/26855877/" target="_blank" rel="noopener">resolutions from the Graduate Student Council</a> pressured the university's leaders to retrofit problematic buildings. School administrators collaborated with bird-safe window film company Feather Friendly to <a href="https://www.dukechronicle.com/article/2015/08/birds-fly-free-at-duke-ciemas-adds-patterned-film" target="_blank" rel="noopener">install bird-safe decals at the Fitzpatrick Center</a>.
    </p>

    <figure class="inline-figure">
      <img src="{base}/bird%20story%20photo1.webp" alt="Bird deterrence dotted patterns on windows of Fitzpatrick Center at Duke University" class="inline-image" />
      <figcaption>
        <a href="https://peerj.com/articles/1652/" target="_blank" rel="noopener">Bird deterrence dotted patterns on windows of Fitzpatrick Center at Duke University. (A) Glass passageways. (B) Close up of the dotted pattern.</a> <em>Photos by Casey Collins.</em>
      </figcaption>
    </figure>

    <p>
      UNC-Chapel Hill is also taking steps to retrofit buildings to reduce bird collisions. City Bird has identified several high-risk university buildings for bird collisions, including Campus Health, Wilson Hall, the FedEx Global Education Center, the Chapman Hall breezeway, Curtis Media Center and the Student Union.
    </p>

    <blockquote>
      "Having data is critically important because we've got to know where we're starting to realize the difference window modifications can make on bird collisions."
      <cite>— Ralph House, Associate Chair for Research, UNC-Chapel Hill Department of Chemistry</cite>
    </blockquote>

    <p>
      "We identified Wilson Hall as one of the priorities for retrofitting," Bai said. "The Department of Biology purchased $5,000 worth of bird-safe film for this purpose, as it is one of the high-collision buildings."
    </p>

    <p>
      House said the real funding barrier is installation, as renting window-washing equipment and hiring professionals can be expensive. "We're working within a small budget," he said.
    </p>

    <p>
      UNC-Chapel Hill's Department of Biology purchased bird-safe window film from CollidEscape. According to Jeff L. Rank, specialty program manager at CollidEscape, material costs range from $2 to $7 per square foot. Installation costs depend on building accessibility and project complexity, ranging from $3 to $6 per square foot. Simpler projects handled by volunteers can cost as little as $2,000 for 1,000 square feet. More complex installations requiring equipment like cranes can cost up to $15,000 for 2,000 square feet.
    </p>

    <blockquote>
      "For probably $10,000 to $15,000, we could really do a lot in terms of getting window films on our windows. If we can help Bai do this and we can get a proof of concept, it'll give us the data that we need to go and apply for more money to show that these window films really do make a difference."
      <cite>— Ralph House, UNC-Chapel Hill</cite>
    </blockquote>

    <p>
      While UNC-Chapel Hill is working to retrofit high-collision buildings like Wilson Hall, other UNC system schools have also implemented bird-safe measures. <a href="https://cdn.shopify.com/s/files/1/0321/1555/6489/files/UNCG_Case_Studies.pdf?v=1713910134" target="_blank" rel="noopener">In 2021, the University of North Carolina Greensboro installed bird-safe films on the Sullivan Science Building after identifying it as a hotspot for bird collisions.</a> The project was funded by UNC Greensboro's Green Fund and supported by the Audubon student group.
    </p>

    <p>
      Applying bird-safe window film effectively prevents bird collisions by breaking up the reflective and transparent qualities of glass that confuse birds. <a href="https://link.springer.com/article/10.1007/s43388-020-00007-0" target="_blank" rel="noopener">Research</a> has shown that patterns like circular decals or vertical and horizontal strips help birds recognize glass as an obstacle. For example, decals spaced at intervals that align with birds' perceptual thresholds (e.g., 2×2-inch or 5×10-cm patterns) create a visual barrier that birds instinctively avoid. Materials such as UV-reflective adhesives leverage birds' ability to see ultraviolet light, making the patterns visible to them while remaining subtle to humans.
    </p>

    <figure class="inline-figure">
      <img src="{base}/bird%20story%20photo2.webp" alt="Samples of products available from Feather Friendly" class="inline-image" />
      <figcaption>
        Samples of some of the products available from Feather Friendly. <em>Photo by <a href="https://www.linkedin.com/in/xiaohuawang/" target="_blank" rel="noopener">Chrissy Wang</a>.</em>
      </figcaption>
    </figure>

    <DataHighlight
      metric="94.3%"
      description="reduction in bird strikes at McCormick Place's Lakeside Center in Chicago after Feather Friendly installed 2×2-inch spacing pattern window films."
      trend="up"
    />

    <p>
      In just one day in October 2023, <a href="https://www.nytimes.com/2023/10/08/us/birds-dead-chicago-building.html" target="_blank" rel="noopener">at least 961 dead birds were documented</a> outside McCormick Place's Lakeside Center in Chicago. Feather Friendly has <a href="https://www.nytimes.com/2024/06/18/us/birds-mass-death-chicago-migration.html" target="_blank" rel="noopener">worked with McCormick Place</a> to install the 2×2-inch spacing pattern window films to reduce bird strikes by 94.3%, according to Paul Groleau, vice president of Feather Friendly.
    </p>

    <p>
      Feather Friendly is also developing new solutions to improve durability and lower costs.
    </p>

    <blockquote>
      "We've been working on a material for about four and a half years that's really going to change the landscape."
      <cite>— Paul Groleau, Vice President, Feather Friendly</cite>
    </blockquote>

    <p>
      Their upcoming innovation aims to extend warranties and enhance the longevity of bird-safe films.
    </p>

    <p>
      If you discover a dead bird after a window collision, Ocampo-Peñuela suggests documenting the incident by photographing the bird in its original location to capture its geolocation and reporting it to platforms like <a href="https://www.inaturalist.org/projects/bird-window-collisions" target="_blank" rel="noopener">iNaturalist's Bird Window Collisions project</a> and <a href="https://dbird.org/" target="_blank" rel="noopener">Dbird</a>. She also suggests donating dead birds to natural history museums. Dead birds can be preserved for scientific research, such as studying bird morphology, genetics and used for taxidermy practice.
    </p>

    <blockquote>
      "I would say that finding a dead bird is one of the most impactful experiences on someone's life. This is why I think people are invested in preventing bird window collisions. It becomes a personal issue. You find a dead bird, maybe pick it up, and it really weighs on you."
      <cite>— Natalia Ocampo-Peñuela, Professor, University of California, Santa Cruz</cite>
    </blockquote>
  </div>
