    <p class="article-lede">
      Every night from 10 p.m. to 2 a.m., whatever the weather, four volunteers drive two cars, patrolling the streets of Sunset Park. They are not police officers, but community volunteers â€” members of a group that has spent 11 years working to keep their community safe.
    </p>

    <p>
      That group is the Brooklyn Asian Civilian Observation Patrol (BACOP), established in late 2013 and officially recognized by the New York Police Department in 2014.
    </p>

    <p>
      Their cars are equipped with lights, sirens, and walkie-talkies but have no bulletproof doors or weapons, since BACOP has no law enforcement power. Volunteers wear uniforms, and their white patrol cars resemble police vehicles, bearing NYPD-approved decals.
    </p>

    <p>
      On patrol, members respond to calls from residents or business owners about fights, minor traffic accidents or public disturbances.
    </p>

    <p>
      "Sometimes people flag us down for fights, fender benders, or someone who's drunk on the street," said Biao Jiang, 43, who joined BACOP in 2018 and is now its director. Jiang, who works in mortgage lending, added: "We go check, and if it's serious, we call the precinct to handle it."
    </p>

    <p>
      "Back then, when we first started, there could be three robberies in a single day along Eighth Avenue," said Xing Sun, 31, a Taekwondo instructor. "Now the whole neighborhood feels much more stable, and we rarely hear about robberies anymore."
    </p>

    <p>
      From the perspective of many local business owners, BACOP seems like an official organization connected to the government.
    </p>

    <p>
      "They should have government funding, but from the beginning, it was a collective effort," said Zheng Lin, owner of the long-established fish ball restaurant Maylin Garden on Eighth Avenue. "For example, at their annual meeting, we all donate. Everyone in our community contributes, and they rely on these funds to keep things going."
    </p>

    <p>
      BACOP says it has never accepted any government funding. Its operations rely entirely on individual member donations and community support. The NYPD's public information office did not respond to repeated requests for comment.
    </p>

    <p>
      "The government has tried many times to give us grants, but our captain doesn't want to take government funding," said Jiang. "So we tell them to give that money to organizations that need it more. As for our operational costs, that's all from our members. We each contribute every year."
    </p>

    <p>
      But while protecting the community, not everyone appreciates BACOP's presence.
    </p>

    <p>
      "Many people say, 'Your lights are too bright and it's flashing in my eyes!'" said Jiang. "But we just want to deter people who might be thinking about committing crimes. We're just ordinary people, earning a living while contributing our time to the community."
    </p>

    <p>
      The team consists of about 50 volunteers. Each night, four of them split into two teams, with each person patrolling for two hours. Their route stretches from 39th Street to 72nd, covering the Chinese business district between Sixth Avenue and Fort Hamilton Parkway, an area under the jurisdiction of the 66th, 68th and 72nd precincts.
    </p>

