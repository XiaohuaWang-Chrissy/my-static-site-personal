    <p>
      Data from NYC Open Data shows that after the BACOP was established at the end of 2013, the number of crimes in the area declined. However, after the pandemic, crime rates began to rise again, a sign the community may still need a safety patrol.
    </p>

    <p>
      Beyond patrolling, members often help immigrants with limited English handle paperwork or procedures.
    </p>

    <p>
      "If it's a traffic incident with no injuries, our job is to help them figure out what to do and where to go," Sun said.
    </p>

    <p>
      Not everyone who applies to join BACOP is accepted.
    </p>

    <p>
      "Some people just want to show off, but we don't want those kinds of people," Sun said. Applicants must go through screening and an interview, followed by training provided by the NYPD.
    </p>

    <p>
      After the screening, both new and old members attend regular training sessions organized by the NYPD. The lessons, offered online and in person, cover community safety, patrolling basics and crisis response. Members learn how to stay alert while on duty, document suspicious activity, and communicate effectively with residents.
    </p>

    <p>
      Sun said members are sincere about helping to run the team and serving the community. "That's all done out of a genuine public spirit," he said.
    </p>
